'''
Tuples

They are just like lists, but you cannot modify their values

Syntax:
tupleName = (initial values separated by commas)
'''

monthsOfYear = (
    'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'
    )

print(monthsOfYear) # ('Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec')
print(monthsOfYear[0]) # Jan
print(monthsOfYear[2:6]) # ('Mar', 'Apr', 'May', 'Jun')


